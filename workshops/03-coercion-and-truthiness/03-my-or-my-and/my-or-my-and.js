function myOr(one, two, three) {
  if (one) {
    return one;
  }
  if (two) {
    return two;
  }
  if (three) {
    return three;
  }
  return three;
}

function myAnd(one, two, three) {
  if (!one) {
    return one;
  }
  if (!two) {
    return two;
  }
  if (!three) {
    return three;
  }
  return three;
}
